"""
Evaluation generator for the Final Submission Report.

Runs 12 pre-defined queries (3 per document class) against the
Query Agent (LangGraph) and dumps the ProvenanceChain results 
into a Markdown report.
"""

from __future__ import annotations

import logging
from pathlib import Path

from dotenv import load_dotenv

from src.agents.query_agent import QueryInterface

# Load environment variables
load_dotenv()

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


# 3 questions per class based on the corpus documents
EVAL_QUERIES = [
    # Class A: CBE Annual Report (Financial)
    "What was the total revenue or total income for CBE in the 2023-24 financial year?",
    "How many branches does CBE operate as of the 2023-24 report?",
    "What are the major highlights regarding digital banking expansion for CBE?",

    # Class B: Audit Report (Scanned)
    "What are the key findings or material weaknesses identified in the audit report?",
    "Who signed off or issued the 2023 Audit Report?",
    "What period does the audit report cover?",

    # Class C: FTA Performance Survey (Mixed layout/Text)
    "According to the FTA performance survey, what is the overall satisfaction score or rate?",
    "What were the main challenges identified by respondents in the FTA survey?",
    "How was the data collected for the 2022 performance survey?",

    # Class D: Tax Expenditure (Table Heavy)
    "What is the total estimated tax expenditure for Ethiopia in 2021/22?",
    "Which sector or tax type accounts for the largest share of tax expenditure?",
    "What is the stated purpose or policy goal of the primary tax exemptions?"
]


def run_evaluations() -> None:
    logger.info("Initializing Query Interface...")
    query_agent = QueryInterface()
    
    out_file = Path("docs/evaluation_qa.md")
    out_file.parent.mkdir(parents=True, exist_ok=True)
    
    logger.info(f"Running {len(EVAL_QUERIES)} evaluations...")
    
    with open(out_file, "w", encoding="utf-8") as f:
        f.write("# Final Submission: Q&A Evaluation Log\n\n")
        f.write("Generated by the Document Intelligence Query Agent (LangGraph).\n\n")
        
        for i, query in enumerate(EVAL_QUERIES, 1):
            logger.info(f"Processing Query {i}/{len(EVAL_QUERIES)}: {query}")
            try:
                chain = query_agent.query(query)
                
                f.write(f"## Query {i}\n")
                f.write(f"**Question:** {chain.query}\n\n")
                f.write(f"**Answer:**\n{chain.answer}\n\n")
                
                status = "✅ YES" if chain.verified else "❌ NO (Unverifiable)"
                f.write(f"**Audit Mode - Supported by Citations?** {status}\n\n")
                
                # Dump full ProvenanceChain citations for rubric compliance
                if chain.citations:
                    f.write("**ProvenanceChain Citations:**\n\n")
                    for j, c in enumerate(chain.citations, 1):
                        f.write(f"- Citation {j}:\n")
                        f.write(f"  - `doc_name`: {c.doc_name}\n")
                        f.write(f"  - `page_number`: {c.page_number}\n")
                        if c.bbox is not None:
                            f.write(
                                f"  - `bbox`: "
                                f"(x0={c.bbox.x0}, y0={c.bbox.y0}, "
                                f"x1={c.bbox.x1}, y1={c.bbox.y1}, "
                                f"page={c.bbox.page_number})\n"
                            )
                        if c.content_hash:
                            f.write(f"  - `content_hash`: `{c.content_hash}`\n")
                        if c.excerpt:
                            f.write(f"  - `excerpt`: {c.excerpt[:200]}\n")
                    f.write("\n")
                
                f.write("---\n")
                
            except Exception as e:
                logger.error(f"Failed query: {query}. Error: {e}")
                f.write(f"## Query {i}\n")
                f.write(f"**Question:** {query}\n\n")
                f.write(f"**Error:** {e}\n\n---\n")

    logger.info(f"Evaluations complete. Results saved to {out_file}")


if __name__ == "__main__":
    run_evaluations()
